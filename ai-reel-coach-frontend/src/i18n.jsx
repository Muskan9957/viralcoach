import { createContext, useContext, useState } from 'react'

const LangCtx = createContext(null)

export function LangProvider({ children }) {
  const [lang, setLang] = useState(() => localStorage.getItem('arc_lang') || 'en')

  const setLanguage = (l) => {
    setLang(l)
    localStorage.setItem('arc_lang', l)
    const base = import.meta.env.VITE_API_URL || ''
    fetch(`${base}/api/user/language`, {
      method: 'PATCH',
      headers: {
        'Content-Type': 'application/json',
        Authorization: `Bearer ${localStorage.getItem('arc_token')}`,
      },
      body: JSON.stringify({ language: l }),
    }).catch(() => {})
  }

  const t = (key) => translations[lang]?.[key] || translations.en[key] || key

  return <LangCtx.Provider value={{ lang, setLanguage, t }}>{children}</LangCtx.Provider>
}

export const useLang = () => useContext(LangCtx)
